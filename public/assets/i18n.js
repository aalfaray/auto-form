function updateContent(lang) {
 const selectedTranslations = translations[lang] || translations['es']

 document.querySelectorAll('[data-i18n]').forEach(element => {
  const key = element.getAttribute('data-i18n')
  const keys = key.split('.')
  let translation = selectedTranslations

  keys.forEach(k => {
   translation = translation ? translation[k] : null
  })

  if (translation) {
   if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
    element.placeholder = translation
   } else {
    element.innerHTML = translation
   }
  }
 })

 // Actualizar los valores del mockup específicamente si existen
 const mockupVals = selectedTranslations.mockup
 if (mockupVals) {
  const inputs = document.querySelectorAll('.filled-field')
  if (inputs.length >= 6) {
   inputs[0].value = mockupVals['val-name']
   inputs[1].value = mockupVals['val-lastname']
   inputs[2].value = mockupVals['val-email']
   inputs[3].value = mockupVals['val-phone']
   inputs[4].value = mockupVals['val-city']
   inputs[5].value = mockupVals['val-address']
  }
 }

 // Actualizar el atributo lang del HTML
 document.documentElement.lang = lang
}

document.addEventListener('DOMContentLoaded', () => {
 const langSelector = document.getElementById('lang-selector')
 const savedLang = localStorage.getItem('preferredLang') || 'es'

 if (langSelector) {
  langSelector.value = savedLang
  updateContent(savedLang)

  langSelector.addEventListener('change', e => {
   const newLang = e.target.value
   localStorage.setItem('preferredLang', newLang)
   updateContent(newLang)
  })
 }
})
